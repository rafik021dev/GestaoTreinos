    private void limparTabela() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnCount(0);
        listaAtual = new ArrayList<>();
        listaAtual.clear();
    }
    
    private void carregarTreinos() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setColumnIdentifiers(new String[]{"Data", "Tipo", "Qtd. Exercícios"}); 
        TreinoController tCont = new TreinoController();
        List<Treino> listaTreino = tCont.listarHistorico(usuario.getIdUsuario());
        
        if (listaTreino != null) {
            for (Treino t : listaTreino) {
                model.addRow(new Object[]{t.getData(), t.getTipo(), (t.getExercicios() != null ? t.getExercicios().size() : 0)});
                listaAtual.add(t);
            }
        }
    }

    private void carregarSonos() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setColumnIdentifiers(new String[]{"Data", "Horas", "Qualidade"});
        
        SonoController sCont = new SonoController();
        List<Sono> listaSono = sCont.listarHistorico(usuario.getIdUsuario());
        if (listaSono != null) {
            for (Sono s : listaSono) {
                model.addRow(new Object[]{s.getData(), s.getHorasDormidas() + "h", s.getQualidade()});
                listaAtual.add(s);
            }
        }
    }
    private void carregarRefeicoes() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setColumnIdentifiers(new String[]{"Data", "Tipo", "Calorias"});   
        RefeicaoController rCont = new RefeicaoController();
        List<Refeicao> lista = rCont.listarHistorico(usuario.getIdUsuario());
        
        if (lista != null) {
            for (Refeicao r : lista) {
                double cals = r.calcularCalorias(); 
                model.addRow(new Object[]{r.getData(), r.getTipo(), String.format(cals + " kcal")});
                listaAtual.add(r);
            }
        }
    }
