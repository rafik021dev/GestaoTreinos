    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        String tipoSelecionado = (String) jCbHistorico.getSelectedItem();
        this.tipoAtual = tipoSelecionado;
        limparTabela();

        try {
            if(tipoAtual.equalsIgnoreCase("Treino")){
                carregarTreinos();
            }else if(tipoAtual.equalsIgnoreCase("Sono")){
                carregarSonos();
            }else if(tipoAtual.equalsIgnoreCase("Refeição")){
                carregarRefeicoes();
            }else{
                JOptionPane.showMessageDialog(this, "Selecione um tipo valido");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }          
    }//GEN-LAST:event_buscarActionPerformed

    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed

        int linha = jTable1.getSelectedRow();
        Object itemExcluir = listaAtual.get(linha);
        if (linha == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um item para excluir");
            return;
        }
        String msg = "Registro excluido.";
        
        try {
            if (itemExcluir instanceof Treino) {
                TreinoController tCont = new TreinoController();
                msg = tCont.excluirTreino(((Treino) itemExcluir).getIdTreino());
            } else if (itemExcluir instanceof Sono) {
                SonoController sCont = new SonoController();
                msg = sCont.excluirSono(((Sono) itemExcluir).getIdSono());
            } else if (itemExcluir instanceof Refeicao) {
                RefeicaoController rCont = new RefeicaoController();
                msg = rCont.excluirRefeicao(((Refeicao) itemExcluir).getIdRefeicao());
            }
            JOptionPane.showMessageDialog(this, msg);
            buscarActionPerformed(null);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_excluirActionPerformed

